def get_template(name):
    templates = {
        "жалоба": (
            "ЖАЛОБА\n\n"
            "В [название органа]\n"
            "От: [ФИО, адрес, телефон]\n\n"
            "Суть жалобы: [описание ситуации]\n\n"
            "Прошу принять меры.\n\n"
            "Дата: [дата]    Подпись: [подпись]"
        )
    }
    return templates.get(name.lower(), "Шаблон не найден.")
